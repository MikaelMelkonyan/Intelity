import UIKit

final class LoadingCell: SimpleCell {
    
    @IBOutlet weak private var activityIndicator: UIActivityIndicatorView!
    
    func startAnimating() {
        activityIndicator.startAnimating()
    }
    
    func stopAnimating() {
        activityIndicator.stopAnimating()
    }
}
